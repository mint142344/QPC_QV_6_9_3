// uart receive
void USART1_IRQHandler(void)
{
    if (USART_GetITStatus(USART1, USART_IT_RXNE)) {
        uint8_t rx = USART_ReceiveData(USART1);
        switch (rx) {
            case 0: {
            } break;
            case 1: {
                LED_Toggle(LED_GREEN);
            } break;
            default:
                break;
        }
    }
}

